package com.june.app.domain;


public class Page {

	
	private int seq;
	private int start;
	private int limit;
	private int pageper;
	private int totcnt;
	
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getLimit() {
		return limit;
	}
	public void setLimit(int limit) {
		this.limit = limit;
	}
	public int getPageper() {
		return pageper;
	}
	public void setPageper(int pageper) {
		this.pageper = pageper;
	}
	public int getTotcnt() {
		return totcnt;
	}
	public void setTotcnt(int totcnt) {
		this.totcnt = totcnt;
	}
	
	
	
	
	
	
	

	
}
